/*
 * Бегущие огоньки (Atmega32)
 * Два светодиода сдвигаются сверху вниз, затем снизу вверх
 * Частота МК: 1 МГц
 */

#include <avr/io.h>
#define F_CPU 1000000UL
#include <util/delay.h>

int main(void)
{
    DDRD = 0xFF;          // все выводы порта D на выход
    signed char i;        // знаковая переменная для циклов

    while (1)
    {
        // движение сверху вниз
        for (i = 0; i <= 6; i++)
        {
            PORTD = (3 << i);   // зажигаем два соседних светодиода
            _delay_ms(250);
        }

        // движение снизу вверх
        for (i = 6; i >= 0; i--)
        {
            PORTD = (3 << i);
            _delay_ms(250);
        }
    }
}
