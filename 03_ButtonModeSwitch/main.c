/*
 * Переключение режимов свечения (Atmega32)
 * По нажатию кнопки на PB0 переключается режим свечения светодиодов на порту D
 * Частота МК: 1 МГц
 */

#include <avr/io.h>
#define F_CPU 1000000UL
#include <util/delay.h>

// Инициализация портов
void Port_ini(void)
{
    DDRD = 0xFF;               // светодиоды на порту D
    DDRB &= ~(1 << 0);         // кнопка на PB0 (вход)
    PORTB |= (1 << 0);         // подтяжка
}

int main(void)
{
    Port_ini();
    signed char i, n = 0;      // n — номер режима (0..4)

    while (1)
    {
        // Обработка кнопки с антидребезгом
        if (~PINB & (1 << 0))
        {
            _delay_ms(200);
            if (~PINB & (1 << 0))
            {
                n++;
                if (n > 4) n = 0;
                PORTD = 0x00;     // погасить светодиоды
                while (~PINB & (1 << 0)); // ждать отпускания
            }
        }

        // Реализация режима свечения
        switch (n)
        {
            case 0:
                // Сдвиг одного огонька сверху вниз
                for (i = 0; i <= 7; i++)
                {
                    PORTD = (1 << i);
                    _delay_ms(250);
                }
                break;

            case 1:
                // Сдвиг двух огоньков навстречу друг другу
                for (i = 0; i <= 6; i++)
                {
                    PORTD = (1 << i) | (1 << (7 - i));
                    _delay_ms(250);
                }
                break;

            case 2:
                // Зажигание всех сверху вниз, затем гашение снизу вверх
                for (i = 0; i <= 7; i++)
                {
                    PORTD |= (1 << i);
                    _delay_ms(250);
                }
                for (i = 7; i >= 0; i--)
                {
                    PORTD &= ~(1 << i);
                    _delay_ms(250);
                }
                break;

            case 3:
                // Расхождение от центра к краям и обратное схождение
                for (i = 0; i <= 3; i++)
                {
                    PORTD |= (1 << (3 - i)) | (1 << (4 + i));
                    _delay_ms(250);
                }
                for (i = 3; i >= 0; i--)
                {
                    PORTD &= ~((1 << (3 - i)) | (1 << (4 + i)));
                    _delay_ms(250);
                }
                break;

            case 4:
                // Мигание всеми светодиодами
                PORTD = 0xFF;
                _delay_ms(250);
                PORTD = 0x00;
                _delay_ms(250);
                break;
        }
    }
}